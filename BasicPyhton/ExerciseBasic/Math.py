
"""Math Library"""
import math

pi = 3.14
print(round(pi))  # Girilen ondalıklı değeri en yakın tam sayıya yuvarlar = 3
print(math.ceil(pi))  # Girilen ondalıklı değerin en yakın üst tam sayıya yuvarlar = 4
print(math.floor(pi))  # Girilen ondalıklı değerin en yakın alt tam sayıya yuvarlar = 3
print(pow(pi, 2))  # Değerimizin üssünü almak için pow fonksiyonu kullanırız = 9.8596

a = -3.14
print(abs(a))  # Girilen negatif mutlak değer sayının pozitifini yazdırır = 3.14
b = 400
print(int(math.sqrt(b)))  # Değerimizin karekökünü almak için sqrt fonksiyonu kullanırız = 20

c = 1
d = 2
e = 3
print(max(c, d, e))  # Değerlerimizin maksimum olanı almak için max fonksiyonu kullanırız = 3
print(min(c, d, e))  # Değerlerimizin minimum olanı almak için min fonksiyonu kullanırız = 1



